// ─────────────────────────────────────────────────────────────────────────────
// import
// ─────────────────────────────────────────────────────────────────────────────

const { gql } = require('apollo-server-lambda');

// ─────────────────────────────────────────────────────────────────────────────
// db
// ─────────────────────────────────────────────────────────────────────────────

const letters = ['a', 'b', 'c'];

// ─────────────────────────────────────────────────────────────────────────────
// typeDefs
// ─────────────────────────────────────────────────────────────────────────────

exports.typeDefs = gql`
  extend type Query {
    alphabet: [String]
  }

  extend type Mutation {
    addLetter(letter: String): String
  }
`;

// ─────────────────────────────────────────────────────────────────────────────
// resolvers
// ─────────────────────────────────────────────────────────────────────────────

exports.resolvers = {
  Query: {
    alphabet: () => letters,
  },
  Mutation: {
    addLetter: (parent, { letter }) => {
      letters.push(letter);
      return letter;
    },
  },
};
